async function main() {
  const p = new PrismaClient({ adapter: new PrismaNeon({ connectionString: process.env.DATABASE_URL! }) });
  const apply = process.argv[2] === "apply";

  let updated = 0;
  let missing: string[] = [];

  for (const [slug, content] of Object.entries(CONTENT)) {
    const prod = await p.product.findUnique({ where: { slug } });
    if (!prod) {
      missing.push(slug);
      continue;
    }
    if (apply) {
      await p.product.update({
        where: { slug },
        data: {
          description: content.description,
          longDescription: content.longDescription,
          deliveryInfo: content.deliveryInfo,
          guaranteeInfo: content.guaranteeInfo,
          faqs: content.faqs,
          keywords: content.keywords,
        },
      });
      console.log(`✓ ${slug}: ${prod.name}`);
    } else {
      console.log(`[미리보기] ${slug}: ${content.description.length}자 / long ${content.longDescription.length}자 / faqs ${content.faqs.length}건`);
    }
    updated++;
  }

  console.log(`\n총 ${updated}건 ${apply ? '적용 완료' : '대상'}`);
  if (missing.length) console.log(`못 찾음: ${missing.join(", ")}`);
  if (!apply) console.log(`\n실제 적용하려면 인자 "apply" 붙여서 다시 실행`);
  await p.$disconnect();
}
main().catch(console.error);
